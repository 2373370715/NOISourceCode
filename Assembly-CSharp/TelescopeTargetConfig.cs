		gameObject.AddOrGet<TelescopeTarget>();
		return gameObject;
	}

	public void OnPrefabInit(GameObject inst)
	{
	}

	public void OnSpawn(GameObject inst)
	{
	}

	public const string ID = "TelescopeTarget";
}
